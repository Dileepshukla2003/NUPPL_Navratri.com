function el(q){return document.querySelector(q)}

function createDayCard(d){
  const card = document.createElement('div');
  card.className = 'day-card';
  card.setAttribute('data-day', d.day);
  card.innerHTML = `
    <div class="day-thumb">
      <img src="${d.image}" alt="${d.title}">
    </div>
    <div class="day-info">
      <h3>Day ${d.day}: ${d.title}</h3>
      <p>${d.short}</p>
      <p class="muted">${d.schedule}</p>
    </div>
  `;
  card.addEventListener('click', ()=> openModal(d));
  return card;
}

function openModal(d){
  const modal = el('#modal');
  const body = el('#modalBody');
  modal.classList.add('show');
  modal.setAttribute('aria-hidden','false');
  body.innerHTML = `
    <div class="modal-body">
      <div class="modal-thumb">
        <img src="${d.image}" alt="${d.title}" style="width:100%; height:100%; object-fit:cover; border-radius:12px; min-height:240px;">
      </div>
      <div>
        <h2>Day ${d.day}: ${d.title}</h2>
        <p class="muted">${d.short}</p>
        <p>${d.description}</p>
        <p><strong>Schedule:</strong> ${d.schedule}</p>
        <p><strong>Venue: </strong> NUPPL Temple Permises & Community hall (For Dinner)</p>
        <h4>Program highlights</h4>
        <ul>
          ${d.program.map(item=>`<li>${item}</li>`).join('')}
        </ul>
      </div>
    </div>
  `;
}

function closeModal(){
  const modal = el('#modal');
  modal.classList.remove('show');
  modal.setAttribute('aria-hidden','true');
}

function populate(){
  const grid = el('#daysGrid');
  days.forEach(d=>{
    grid.appendChild(createDayCard(d));
  });
}

document.addEventListener('DOMContentLoaded', ()=>{
  populate();
  el('#modalClose').addEventListener('click', closeModal);
  el('#modal').addEventListener('click', (e)=>{ if(e.target.id==='modal') closeModal(); });
  el('.mobile-toggle').addEventListener('click', ()=> {
    const nav = document.querySelector('.nav-links');
    nav.style.display = nav.style.display === 'flex' ? 'none' : 'flex';
  });
});
